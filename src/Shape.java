/**
 * This interface represents a Shape object. A Shape object represents a shape
 * (like a line or circle or rectangle) that can be displayed graphically or by
 * listing its relevant pointson the console. The display mode (graphical or
 * listing) is at the discretion of the implementor.
 * 
 * @author Carter Clark
 *
 */
public interface Shape {

	/**
	 * This method displays the points of the given shape object
	 */
	public void display();
}
